    Побажання = models.TextField() #побажання#
    update = models.DateTimeField(auto_now=True) #дата оновлення замовлення#
    timestamp = models.DateTimeField(auto_now_add=True) #дата створення замовлення#


    def __str__(self):
        return self.ПІБ #повертає об'єкт по ПІБ#

