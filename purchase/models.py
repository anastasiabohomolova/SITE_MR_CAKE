class Purchase(models.Model): #модель для оформлення замовлення#
    ПІБ = models.CharField(max_length=100) #прізвище, ім'я замовника#
    Адреса = models.CharField(max_length=100) #адреса замовника#
    Телефон  = models.IntegerField() #телефон замовника#
    Назва_торта = models.CharField(max_length=100) #назва торта#
    Категорія = models.CharField(max_length=100, choices=type_category(), default='') #категорія торта#
